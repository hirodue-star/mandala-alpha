  /// Claude API で連想アイテムを8つ取得
  static Future<List<String>> fetchFromApi(String theme, {String? apiKey}) async {
    if (apiKey == null || apiKey.isEmpty) return getLocalSuggestions(theme);

    try {
      final response = await http.post(
        Uri.parse(_endpoint),
        headers: {
          'x-api-key': apiKey,
          'anthropic-version': '2023-06-01',
          'content-type': 'application/json',
        },
        body: jsonEncode({
          'model': _model,
          'max_tokens': 256,
          'system': 'あなたは3〜5歳の幼児教育の専門家です。',
          'messages': [
            {
              'role': 'user',
              'content': '''
「$theme」に関連する、3〜5歳の幼児が理解できる具体的なアイテム・行動を8つ挙げてください。

ルール:
- ひらがな・カタカナのみ（漢字不可）
- 各アイテムは5文字以内
- ポジティブな内容のみ
- 1行1アイテム、番号なし
- 余計な説明は不要

例（テーマ:そうじ）:
ほうき
ぞうきん
そうじき
バケツ
ゴミばこ
ふく
あらう
ピカピカ
''',
            },
          ],
        }),
      ).timeout(const Duration(seconds: 10));

      if (response.statusCode == 200) {
        final json = jsonDecode(response.body) as Map<String, dynamic>;
        final text = (json['content'] as List).first['text'] as String;
        final items = text
            .split('\n')
            .map((l) => l.trim())
            .where((l) => l.isNotEmpty && l.length <= 8)
            .take(8)
            .toList();
        if (items.length >= 3) return items;
      }
    } catch (_) {
      // フォールバック
    }
    return getLocalSuggestions(theme);
  }

  /// ローカル辞書フォールバック（オフライン/APIキーなし）
  static List<String> getLocalSuggestions(String theme) {
    final t = theme.toLowerCase();

    // キーワードマッチング
    for (final entry in _dictionary.entries) {
      for (final keyword in entry.key) {
        if (t.contains(keyword)) return entry.value;
      }
    }

    // マッチなし → 汎用
    return _genericSuggestions;
  }

